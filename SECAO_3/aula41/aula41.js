// Escreva uma função que recebe 2 números e retorne o maior deles

// Usando função clássica
function maiorValor1(num1, num2) {
  return Math.max(num1, num2);
}

console.log(maiorValor1(100, 100));

// Usando arrow function
const maiorValor2 = (a, b) => Math.max(a, b);


console.log(maiorValor2(150, 33));
